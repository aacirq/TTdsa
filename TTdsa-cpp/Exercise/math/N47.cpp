#include "ttdsa.h"

class Solution {
public:
    int Sum_Solution(int n) {
        int x = n + 1;
        int res = 0;
        (n & (1)) && (res += x);
        (n & (1 << 1)) && (res += (x << 1));
        (n & (1 << 2)) && (res += (x << 2));
        (n & (1 << 3)) && (res += (x << 3));
        (n & (1 << 4)) && (res += (x << 4));
        (n & (1 << 5)) && (res += (x << 5));
        (n & (1 << 6)) && (res += (x << 6));
        (n & (1 << 7)) && (res += (x << 7));
        (n & (1 << 8)) && (res += (x << 8));
        (n & (1 << 9)) && (res += (x << 9));
        (n & (1 << 10)) && (res += (x << 10));
        (n & (1 << 11)) && (res += (x << 11));
        (n & (1 << 12)) && (res += (x << 12));
        (n & (1 << 13)) && (res += (x << 13));
        (n & (1 << 14)) && (res += (x << 14));
        (n & (1 << 15)) && (res += (x << 15));
        (n & (1 << 16)) && (res += (x << 16));
        (n & (1 << 17)) && (res += (x << 17));
        (n & (1 << 18)) && (res += (x << 18));
        (n & (1 << 19)) && (res += (x << 19));
        (n & (1 << 20)) && (res += (x << 20));
        (n & (1 << 21)) && (res += (x << 21));
        (n & (1 << 22)) && (res += (x << 22));
        (n & (1 << 23)) && (res += (x << 23));
        (n & (1 << 24)) && (res += (x << 24));
        (n & (1 << 25)) && (res += (x << 25));
        (n & (1 << 26)) && (res += (x << 26));
        (n & (1 << 27)) && (res += (x << 27));
        (n & (1 << 28)) && (res += (x << 28));
        (n & (1 << 29)) && (res += (x << 29));
        (n & (1 << 30)) && (res += (x << 30));
        (n & (1 << 31)) && (res += (x << 31));
        return res >> 1;
    }
};

int main() {
    Solution sol;
    cout << sol.Sum_Solution(5) << endl;
    return 0;
}